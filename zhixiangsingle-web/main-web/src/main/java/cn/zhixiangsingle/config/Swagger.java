@Configuration
@EnableSwagger2
public class Swagger {
    /**
     * @version V1.0
     * @Title: zhixiangpingtai
     * @Description:
     * @author: hhp
     * @param:  * @param
     * @date: 2019/8/23 9:27
     */
    @Bean
    public Docket createRestApi() {
        return new Docket(DocumentationType.SWAGGER_2)
                .apiInfo(apiInfo())
                .select()
                .apis(RequestHandlerSelectors.basePackage("cn.zhixiangsingle"))
                .paths(PathSelectors.any())
                .build();
    }
    /**
     * @version V1.0
     * @Title: zhixiangpingtai
     * @Description:
     * @author: hhp
     * @param:  * @param
     * @date: 2019/8/23 9:27
     */
    private ApiInfo apiInfo() {
        return new ApiInfoBuilder()
                .title("Spring Boot USES Swagger2 to build RESTful APIs")
                .description("For more Spring Boot related articles:https://blog.csdn.net/hhppppppp")
                .termsOfServiceUrl("https://blog.csdn.net/hhppppppp")
                .contact("hhp")
                .version("1.0")
                .build();
    }
}
